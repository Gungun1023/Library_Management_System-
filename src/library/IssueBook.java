    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connect();
            String BookId = bookid.getText();
            String StudentId = stuid.getText();

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String Issuedate = sdf.format(issuedate.getDate());
            String Returndate = sdf.format(returndate.getDate());

            pst = con.prepareStatement("SELECT * FROM add_books WHERE BookId= ?");
            pst.setString(1, BookId);
            rs = pst.executeQuery();
            if (rs.next()) {
                // Check if the student exists
                pst = con.prepareStatement("SELECT * FROM student_registration WHERE StudentId  = ?");
                pst.setString(1, StudentId);
                rs = pst.executeQuery();
                if (rs.next()) {
                    // Insert issue record
                    pst = con.prepareStatement("INSERT INTO issue_book ( BookID, StudentID, IssueDate, ReturnDate) VALUES (?, ?, ?, ?)");
                    pst.setString(1, BookId);
                    pst.setString(2, StudentId);
                    pst.setString(3, Issuedate);
                    pst.setString(4, Returndate);
                    pst.executeUpdate();

                    JOptionPane.showMessageDialog(this, "ISSUE GRANTED!!!");
                    new IssueBook().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "INCORRECT STUDENT ID!!!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "INCORRECT BOOK ID!!!");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        } finally {
            // Close resources
            try {
                if (rs != null) {
                    rs.close();
                }
                if (pst != null) {
                    pst.close();
                }
                if (con != null) {
                    con.close();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error closing resources: " + e.getMessage());
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed


    private void table_update() {
        int c;
        try 
        {
            // Corrected SQL query with appropriate JOIN conditions and column aliases
            pst = con.prepareStatement("SELECT i.IssueID AS issueid, i.BookID AS bookid, i.StudentID AS studentid, i.IssueDate AS issuedate, i.ReturnDate AS returndate "
                    + "FROM issue_book i "
                    + "JOIN add_books a ON i.BookID = a.BookId "
                    + "JOIN student_registration sr ON i.StudentID = sr.StudentId");

            ResultSet rs = pst.executeQuery();
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel) jTable1.getModel();
            d.setRowCount(0);

            while (rs.next()) {
                Vector<String> v2 = new Vector<>();
                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString(i));
                }
                d.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


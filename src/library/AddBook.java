    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String BookIdText = bookid.getText();
            int BookId = Integer.parseInt(BookIdText);
            String BookName = bookname.getText();
            String Publisher = publisher.getText();

            String PriceText = price.getText();
            int Price = Integer.parseInt(PriceText);

            String YearText = year.getText();
            int Year = Integer.parseInt(YearText);

            PreparedStatement pst = con.prepareStatement("INSERT INTO add_books(`BookId`, `BookName`, `Publisher`, `Price`, `Year`) VALUES (?, ?, ?, ?, ?)");

            pst.setInt(1, BookId);
            pst.setString(2, BookName);
            pst.setString(3, Publisher);
            pst.setInt(4, Price);
            pst.setInt(5,  Year);

            try {
               
                int k = pst.executeUpdate();

            
                if (k == 1) {
                   
                    JOptionPane.showMessageDialog(this, "BOOK ADDED SUCCESSFULLY!!!");

                  bookname.setText("");
                publisher.setText("");
                price.setText("");
                year.setText("");
                bookname.requestFocus();                

                   
                    table_update();
                } else {
                    
                    JOptionPane.showMessageDialog(this, "ERROR");
                }
            } catch (SQLException e) {
               
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, " ERROR ID WAS SAME!!! " );
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

